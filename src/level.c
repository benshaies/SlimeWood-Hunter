Texture2D floors, wall;

void levelInit(){
    floors = LoadTexture("../Assets/floor.png");
    wall = LoadTexture("../Assets/wall.png");
}

 void drawLevel(int currentLevel[20][20]){
    for(int y = 0; y < LEVELSIZE; y++){
        for(int x = 0; x < LEVELSIZE; x++){
            
            if(currentLevel[y][x] == 0){
                DrawTexturePro(floors, (Rectangle){0,0,16,16}, (Rectangle){x*50, y*50, 50, 50}, (Vector2){0,0}, 0, WHITE);
            }
            else if(currentLevel[y][x] == 1){
                DrawTexturePro(wall, (Rectangle){0,0,16,16}, (Rectangle){x*50, y*50, 50, 50}, (Vector2){0,0}, 0, WHITE);
            }
            else if(currentLevel[y][x] == 2){
                DrawTexturePro(floors, (Rectangle){16,0,16,16}, (Rectangle){x*50, y*50, 50, 50}, (Vector2){0,0}, 0, WHITE);
            }
            else if(currentLevel[y][x] == 3){
                DrawTexturePro(floors, (Rectangle){32,0,16,16}, (Rectangle){x*50, y*50, 50, 50}, (Vector2){0,0}, 0, WHITE);
            }
            else if(currentLevel[y][x] == 4){
                DrawTexturePro(floors, (Rectangle){48,0,16,16}, (Rectangle){x*50, y*50, 50, 50}, (Vector2){0,0}, 0, WHITE);
            }

        }
    }
 }